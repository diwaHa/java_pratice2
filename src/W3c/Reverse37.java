package W3c;

public class Reverse37 {
 public static void main (String args[]) {
	 String s = "The quick brown fox";
	 // by using lenght of string 
	 //   and by using char datatype to intialze each character in the string
	for(int i =s.length()- 1;i>=0;i--) {
		System.out.print(s.charAt(i));
		
	}
	
 }
}  


